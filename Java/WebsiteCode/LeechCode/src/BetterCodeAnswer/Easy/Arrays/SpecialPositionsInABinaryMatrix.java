public class SpecialPositionsInABinaryMatrix {
    public static void main(String[] args) {
        int[][][] tests = {
            {{1,0,0},{0,0,1},{1,0,0}},
            {{1,0,0},{0,1,0},{0,0,1}},
        };

        for (int[][] matrix : tests) {
            System.out.println(new SpecialPositionsInABinaryMatrix_Solution().numSpecial(matrix));
        }
    }
}

// 2 ms 44.1 MB
class SpecialPositionsInABinaryMatrix_Solution {
    public int numSpecial(int[][] mat) {

        int specials = 0;

        for (int i = 0; i < mat.length; i++) {
            int index = checkRow(mat, i);
            if (index >= 0 && checkColumn(mat, i, index))
                specials++;
        }

        return specials;
    }

    private int checkRow(int[][] mat, int i) {
        int index = -1;
        for (int j = 0; j < mat[0].length; j++) {
            if (mat[i][j] == 1) {
                if (index >= 0)
                    return -1;
                else
                    index = j;
            }
        }
        return index;
    }

    private boolean checkColumn(int[][] mat, int i, int index) {
        for (int j = 0; j < mat.length; j++) {
            if (mat[j][index] == 1 && j != i)
                return false;
        }
        return true;
    }
}
        
