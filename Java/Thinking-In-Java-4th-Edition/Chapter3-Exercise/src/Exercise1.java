// Write a program that prints values from 1 to 100. 

public class Exercise1 {
    public static void main(String[] args) {
        System.out.println("Exercise 1 Result: ");

        for (int i = 0; i < 100; i++) {
            System.out.println(i+1);
        }

        System.out.println("----------------------------------------------------");
    }
}
